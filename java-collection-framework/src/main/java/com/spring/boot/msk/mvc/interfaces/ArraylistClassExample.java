package com.spring.boot.msk.mvc.interfaces;

import java.util.ArrayList;
import java.util.List;

//How to create an arraylist using Arraylist() constructor
//add new elements to an Araylist using thee add() method

public class ArraylistClassExample {

	public static void main(String[] args) {
		
		//created arraylist object
		List<String> fruits= new ArrayList<>();
		
		//add elements to arraylist object
		
		fruits.add("Banana");
		fruits.add("Apple");
		fruits.add("mango");
		fruits.add("orange");
		
		System.out.println(fruits);
		
		
		
		
	}

}
